primer_entero = int(input("ingrese un numero entero:"))
segundo_entero = int(input("ingrese otro entero:"))
primer_calculo = primer_entero // segundo_entero
resto = primer_entero % segundo_entero
print(f"El numero {primer_entero} dividido  por {segundo_entero} es igual a: {primer_calculo} y el resto es de : {resto}")